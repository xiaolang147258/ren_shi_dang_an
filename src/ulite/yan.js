/**
 * 数字
 */
export function isNumber (s) {
  return /^[0-9]*$/.test(Number(s))
}
/**
 * 整数
 */
export function isInteger (s) {
  return /^-?\d+$/.test(Number(s))
}
/**
 * 正整数
 */
export function isZInteger (s) {
  return /^\d+$/.test(Number(s))
}
/**
 * 负整数
 */
export function isFInteger (s) {
  return /^((-\d+)|(0+))$/.test(Number(s))
}
/**
 * 浮点数
 */
export function isFloat (s) {
  return /^(-?\d+)(\.\d+)?$/.test(Number(s))
}
